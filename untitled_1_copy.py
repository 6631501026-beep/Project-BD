# ==========================================
# 4. PREDICT FUNCTION
# ==========================================
def ask_question(message):
    """ฟังก์ชันสำหรับรับคำถามและคืนค่าคำตอบผ่าน RAG"""
    try:
        has_thai_chars = bool(re.search(r'[฀-๿]', message))
        lang_instruction = " (ตอบเป็นภาษาไทยเท่านั้น)" if has_thai_chars else " (Answer in English strictly and concisely.)"

        response = qa_chain.invoke({"input": message + lang_instruction})
        return response['answer'].strip()
    except Exception as e:
        return f"❌ เกิดข้อผิดพลาด: {str(e)}"


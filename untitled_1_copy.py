import zipfile
import os
from google.colab import files

# Define your project files
# Added the .docx file and the folder logic
files_to_zip = [
    'app.py',
    'requirements.txt',
    'TOEIC_RAG_QA_Bilingual.txt'
]

folder_to_zip = 'faiss_index'
zip_name = 'BDA_Project2_Group5.zip'

with zipfile.ZipFile(zip_name, 'w', zipfile.ZIP_DEFLATED) as zipf:
    # 1. Add individual files
    for file in files_to_zip:
        if os.path.exists(file):
            zipf.write(file)
            print(f"Added file: {file}")
        else:
            print(f"⚠️ Warning: {file} not found!")

    # 2. Add the FAISS folder and its contents
    if os.path.exists(folder_to_zip):
        for root, dirs, files_in_folder in os.walk(folder_to_zip):
            for file in files_in_folder:
                file_path = os.path.join(root, file)
                # This ensures the folder structure is preserved in the zip
                zipf.write(file_path, os.path.relpath(file_path, os.path.join(folder_to_zip, '..')))
        print(f"Added folder: {folder_to_zip}")

print(f"\n✅ ZIP created: {zip_name}")

# Trigger download to your computer
files.download(zip_name)